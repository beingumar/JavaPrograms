public class Maths {
  public void add(int a, int b) {
    System.out.println("The addition is: "+(a + b));
  }

  public void sub(int a, int b) {
    System.out.println("The Subtraction is:"+ (a-b));
  }

  public void mul(int a, int b) {
    System.out.println("The Multiplication is: "+(a*b));
  }

  public void div(int a, int b) 
  {
    System.out.println("The Division is: "+(a / b));
  }

  public void square(int a,int b) {
    System.out.println("The square of" +a+ "is :"+ (a * a));
    System.out.println("The square of" +b+ "is :"+ (b * b));
  }

  public void cube(int a,int b) {
    System.out.println("The Cube of "+ a +" is: "+( a * a * a));
    System.out.println("The Cube of "+ b +" is: "+( b * b * b));
  }
}